#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
SDK_TEST_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
ENGINE_DIR="$(cd "$SDK_TEST_DIR/.." && pwd)"
RUNNER="$ENGINE_DIR/s/test"

TMP_DIR="$(mktemp -d)"
BIN_DIR="$TMP_DIR/bin"
OUTPUT_FILE="$TMP_DIR/output.log"
NPM_LOG="$TMP_DIR/npm.log"
NODE_LOG="$TMP_DIR/node.log"

SDK_NODE_MODULES="$ENGINE_DIR/sdk_test/node_modules"
SDK_NODE_MODULES_BACKUP="$TMP_DIR/sdk_node_modules.backup"
DASHBOARD_NODE_MODULES="$ENGINE_DIR/dashboard/node_modules"
DASHBOARD_NODE_MODULES_CREATED=false
SDK_NODE_MODULES_WAS_PRESENT=false

cleanup() {
  rm -rf "$SDK_NODE_MODULES"

  if [ "$SDK_NODE_MODULES_WAS_PRESENT" = "true" ]; then
    mv "$SDK_NODE_MODULES_BACKUP" "$SDK_NODE_MODULES"
  fi

  if [ "$DASHBOARD_NODE_MODULES_CREATED" = "true" ]; then
    rm -rf "$DASHBOARD_NODE_MODULES"
  fi

  rm -rf "$TMP_DIR"
}
trap cleanup EXIT

mkdir -p "$BIN_DIR"

if [ -e "$SDK_NODE_MODULES" ]; then
  SDK_NODE_MODULES_WAS_PRESENT=true
  mv "$SDK_NODE_MODULES" "$SDK_NODE_MODULES_BACKUP"
fi

if [ ! -d "$DASHBOARD_NODE_MODULES" ]; then
  mkdir -p "$DASHBOARD_NODE_MODULES"
  DASHBOARD_NODE_MODULES_CREATED=true
fi

cat > "$BIN_DIR/curl" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
if [[ "$*" == *"/health"* ]]; then
  exit 0
fi
if [[ "$*" == *"/1/keys"* ]]; then
  exit 0
fi
exit 0
WRAP
chmod +x "$BIN_DIR/curl"

cat > "$BIN_DIR/npm" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf 'cwd=%s args=%s\n' "$PWD" "$*" >> "$STUB_NPM_LOG"
if [[ "${1:-}" == "ci" ]]; then
  mkdir -p "$PWD/node_modules/algoliasearch" "$PWD/node_modules/dotenv"
  exit 0
fi
exit 9
WRAP
chmod +x "$BIN_DIR/npm"

cat > "$BIN_DIR/node" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf '%s\n' "$*" >> "$STUB_NODE_LOG"
if [[ "${1:-}" == "--version" ]]; then
  echo "v20.10.0"
  exit 0
fi
if [[ "${1:-}" == "-e" ]]; then
  exit 0
fi
if [[ "${1:-}" == "test.js" ]]; then
  exit 99
fi
exit 0
WRAP
chmod +x "$BIN_DIR/node"

set +e
PATH="$BIN_DIR:$PATH" \
STUB_NPM_LOG="$NPM_LOG" \
STUB_NODE_LOG="$NODE_LOG" \
"$RUNNER" --sdk >"$OUTPUT_FILE" 2>&1
status=$?
set -e

if [ "$status" -eq 0 ]; then
  echo "Expected s/test --sdk to fail after reaching stubbed node test.js"
  exit 1
fi

if ! grep -q "cwd=$ENGINE_DIR/sdk_test args=ci" "$NPM_LOG"; then
  echo "Expected runner to run npm ci in engine/sdk_test before JS SDK tests"
  cat "$NPM_LOG"
  cat "$OUTPUT_FILE"
  exit 1
fi

if [ ! -d "$SDK_NODE_MODULES" ]; then
  echo "Expected sdk_test/node_modules to be created"
  exit 1
fi

if [ -L "$SDK_NODE_MODULES" ]; then
  echo "Expected sdk_test/node_modules to be a real directory, not a dashboard symlink"
  ls -la "$SDK_NODE_MODULES"
  exit 1
fi

if [ ! -d "$SDK_NODE_MODULES/algoliasearch" ] || [ ! -d "$SDK_NODE_MODULES/dotenv" ]; then
  echo "Expected sdk_test bootstrap to create algoliasearch and dotenv dependency directories"
  ls -la "$SDK_NODE_MODULES"
  exit 1
fi

if grep -q "Linked sdk_test/node_modules -> ../dashboard/node_modules" "$OUTPUT_FILE"; then
  echo "Runner should not log dashboard node_modules linking behavior"
  cat "$OUTPUT_FILE"
  exit 1
fi

if ! grep -q "require.resolve" "$NODE_LOG"; then
  echo "Expected runner to verify sdk_test dependency resolution via node -e require.resolve guard"
  cat "$NODE_LOG"
  exit 1
fi

if ! grep -q "test.js" "$NODE_LOG"; then
  echo "Expected --sdk execution to reach the JS SDK entrypoint after bootstrap"
  cat "$NODE_LOG"
  exit 1
fi

echo "PASS: s/test bootstraps sdk_test dependencies with npm ci and avoids dashboard node_modules coupling"

#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
SDK_TEST_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
ENGINE_DIR="$(cd "$SDK_TEST_DIR/.." && pwd)"
RUNNER="$ENGINE_DIR/s/test"

TMP_DIR="$(mktemp -d)"
BIN_DIR="$TMP_DIR/bin"
OUTPUT_FILE="$TMP_DIR/output.log"
NPM_LOG="$TMP_DIR/npm.log"
NODE_LOG="$TMP_DIR/node.log"

DASHBOARD_NODE_MODULES="$ENGINE_DIR/dashboard/node_modules"
DASHBOARD_NODE_MODULES_BACKUP="$TMP_DIR/dashboard_node_modules.backup"
DASHBOARD_NODE_MODULES_WAS_PRESENT=false

fail_with_logs() {
  local message="$1"
  shift
  echo "$message"
  for log_file in "$@"; do
    [ -f "$log_file" ] && cat "$log_file"
  done
  exit 1
}

assert_file_contains() {
  local file_path="$1"
  local expected="$2"
  local failure_message="$3"
  shift 3
  if ! grep -Fq "$expected" "$file_path"; then
    fail_with_logs "$failure_message" "$file_path" "$@"
  fi
}

cleanup() {
  rm -rf "$DASHBOARD_NODE_MODULES"

  if [ "$DASHBOARD_NODE_MODULES_WAS_PRESENT" = "true" ]; then
    mv "$DASHBOARD_NODE_MODULES_BACKUP" "$DASHBOARD_NODE_MODULES"
  fi

  rm -rf "$TMP_DIR"
}
trap cleanup EXIT

mkdir -p "$BIN_DIR"

if [ -e "$DASHBOARD_NODE_MODULES" ]; then
  DASHBOARD_NODE_MODULES_WAS_PRESENT=true
  mv "$DASHBOARD_NODE_MODULES" "$DASHBOARD_NODE_MODULES_BACKUP"
fi

# Reuse-path setup: real directory exists, but it is stale and missing required packages.
mkdir -p "$DASHBOARD_NODE_MODULES"

cat > "$BIN_DIR/curl" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
if [[ "$*" == *"/health"* ]]; then
  exit 0
fi
if [[ "$*" == *"/1/keys"* ]]; then
  exit 0
fi
exit 0
WRAP
chmod +x "$BIN_DIR/curl"

cat > "$BIN_DIR/npm" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf 'cwd=%s args=%s\n' "$PWD" "$*" >> "$STUB_NPM_LOG"
if [[ "${1:-}" == "ci" ]]; then
  mkdir -p "$PWD/node_modules/vitest" "$PWD/node_modules/@playwright/test"
  exit 0
fi
if [[ "${1:-}" == "run" && "${2:-}" == "test:unit:run" ]]; then
  if [[ -d "$PWD/node_modules/vitest" && -d "$PWD/node_modules/@playwright/test" ]]; then
    exit 99
  fi
  exit 88
fi
exit 0
WRAP
chmod +x "$BIN_DIR/npm"

cat > "$BIN_DIR/node" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf '%s\n' "$*" >> "$STUB_NODE_LOG"
if [[ "${1:-}" == "--version" ]]; then
  echo "v20.10.0"
  exit 0
fi
if [[ "${1:-}" == "-e" ]]; then
  if [[ -d "$PWD/node_modules/vitest" && -d "$PWD/node_modules/@playwright/test" ]]; then
    exit 0
  fi
  exit 42
fi
exit 0
WRAP
chmod +x "$BIN_DIR/node"

set +e
PATH="$BIN_DIR:$PATH" \
STUB_NPM_LOG="$NPM_LOG" \
STUB_NODE_LOG="$NODE_LOG" \
"$RUNNER" --dashboard >"$OUTPUT_FILE" 2>&1
status=$?
set -e

if [ "$status" -eq 0 ]; then
  echo "Expected s/test --dashboard to stop at the stubbed dashboard unit command after stale-node_modules validation"
  exit 1
fi

assert_file_contains \
  "$NPM_LOG" \
  "cwd=$ENGINE_DIR/dashboard args=ci" \
  "Expected runner to reinstall dashboard dependencies when an existing real node_modules directory is stale" \
  "$OUTPUT_FILE"

assert_file_contains \
  "$OUTPUT_FILE" \
  "Reinstalling dashboard dependencies via npm ci (required packages missing)..." \
  "Expected runner to log stale dashboard dependency rehydration"

assert_file_contains \
  "$NODE_LOG" \
  "require.resolve(process.argv[1])" \
  "Expected runner to validate required dashboard packages on the reuse path"
assert_file_contains \
  "$NODE_LOG" \
  "vitest" \
  "Expected runner to validate required dashboard packages on the reuse path"

assert_file_contains \
  "$NPM_LOG" \
  "cwd=$ENGINE_DIR/dashboard args=run test:unit:run" \
  "Expected --dashboard execution to reach npm run test:unit:run after stale dependency rehydration" \
  "$OUTPUT_FILE"

echo "PASS: s/test revalidates and rehydrates stale real dashboard/node_modules directories"

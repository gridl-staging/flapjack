#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
SDK_TEST_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
ENGINE_DIR="$(cd "$SDK_TEST_DIR/.." && pwd)"
RUNNER="$ENGINE_DIR/_dev/s/test"

TMP_DIR="$(mktemp -d)"
BIN_DIR="$TMP_DIR/bin"
OUTPUT_FILE="$TMP_DIR/output.log"
NPM_LOG="$TMP_DIR/npm.log"
NODE_LOG="$TMP_DIR/node.log"

SDK_NODE_MODULES="$ENGINE_DIR/sdk_test/node_modules"
SDK_NODE_MODULES_BACKUP="$TMP_DIR/sdk_node_modules.backup"
SDK_NODE_MODULES_WAS_PRESENT=false
SDK_LOCKFILE="$ENGINE_DIR/sdk_test/package-lock.json"

fail_with_logs() {
  local message="$1"
  shift
  echo "$message"
  for log_file in "$@"; do
    [ -f "$log_file" ] && cat "$log_file"
  done
  exit 1
}

assert_file_contains() {
  local file_path="$1"
  local expected="$2"
  local failure_message="$3"
  shift 3
  if ! grep -Fq "$expected" "$file_path"; then
    fail_with_logs "$failure_message" "$file_path" "$@"
  fi
}

cleanup() {
  rm -rf "$SDK_NODE_MODULES"

  if [ "$SDK_NODE_MODULES_WAS_PRESENT" = "true" ]; then
    mv "$SDK_NODE_MODULES_BACKUP" "$SDK_NODE_MODULES"
  fi

  rm -rf "$TMP_DIR"
}
trap cleanup EXIT

mkdir -p "$BIN_DIR"

if [ -e "$SDK_NODE_MODULES" ]; then
  SDK_NODE_MODULES_WAS_PRESENT=true
  mv "$SDK_NODE_MODULES" "$SDK_NODE_MODULES_BACKUP"
fi

mkdir -p "$SDK_NODE_MODULES/algoliasearch" "$SDK_NODE_MODULES/dotenv"
printf '%s\n' "$(cksum "$SDK_LOCKFILE" | awk '{print $1 ":" $2}')" > "$SDK_NODE_MODULES/.flapjack-package-lock.cksum"
: > "$NPM_LOG"
: > "$NODE_LOG"

cat > "$BIN_DIR/curl" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
if [[ "$*" == *"/health"* ]]; then
  exit 0
fi
if [[ "$*" == *"/1/keys"* ]]; then
  exit 0
fi
exit 0
WRAP
chmod +x "$BIN_DIR/curl"

cat > "$BIN_DIR/npm" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf 'cwd=%s args=%s\n' "$PWD" "$*" >> "$STUB_NPM_LOG"
if [[ "${1:-}" == "ci" ]]; then
  exit 77
fi
exit 0
WRAP
chmod +x "$BIN_DIR/npm"

cat > "$BIN_DIR/node" <<'WRAP'
#!/usr/bin/env bash
set -euo pipefail
printf '%s\n' "$*" >> "$STUB_NODE_LOG"
if [[ "${1:-}" == "--version" ]]; then
  echo "v20.10.0"
  exit 0
fi
if [[ "${1:-}" == "-e" ]]; then
  if [[ -d "$PWD/node_modules/algoliasearch" && -d "$PWD/node_modules/dotenv" ]]; then
    exit 0
  fi
  exit 42
fi
if [[ "${1:-}" == "test.js" ]]; then
  exit 99
fi
exit 0
WRAP
chmod +x "$BIN_DIR/node"

set +e
PATH="$BIN_DIR:$PATH" \
STUB_NPM_LOG="$NPM_LOG" \
STUB_NODE_LOG="$NODE_LOG" \
"$RUNNER" --sdk >"$OUTPUT_FILE" 2>&1
status=$?
set -e

if [ "$status" -eq 0 ]; then
  echo "Expected _dev/s/test --sdk to stop at the stubbed JS entrypoint on a valid cache-hit reuse path"
  exit 1
fi

if grep -Fq "args=ci" "$NPM_LOG"; then
  fail_with_logs \
    "Did not expect npm ci on valid sdk_test node_modules cache hit" \
    "$NPM_LOG" \
    "$OUTPUT_FILE"
fi

assert_file_contains \
  "$NODE_LOG" \
  "require.resolve(process.argv[1])" \
  "Expected runner to validate sdk_test cache-hit dependencies before reuse"
assert_file_contains \
  "$NODE_LOG" \
  "algoliasearch" \
  "Expected runner to validate sdk_test cache-hit dependencies before reuse"

assert_file_contains \
  "$NODE_LOG" \
  "test.js" \
  "Expected --sdk execution to continue into JS SDK test.js after cache-hit validation" \
  "$OUTPUT_FILE"

echo "PASS: _dev/s/test reuses valid sdk_test/node_modules cache and continues into SDK tests"
